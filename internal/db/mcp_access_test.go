// setupMCPAccessFixture creates two MCP servers (global scope), an org, a team,
// and an API key. It returns all IDs needed by CheckMCPAccess tests.
//
// The two servers are named "target-<suffix>" and "other-<suffix>" and their IDs
// are generated by the DB. The caller receives the real IDs via serverA and serverB.
func setupMCPAccessFixture(t *testing.T, d *DB, suffix string) (orgID, teamID, keyID, serverA, serverB string) {
	t.Helper()
	svrA := mustCreateMCPServer(t, d, defaultMCPParams("target-"+suffix))
	svrB := mustCreateMCPServer(t, d, defaultMCPParams("other-"+suffix))
	org := mustCreateOrg(t, d, CreateOrgParams{Name: "Org " + suffix, Slug: "org-mcp-" + suffix})
	team := mustCreateTeam(t, d, CreateTeamParams{OrgID: org.ID, Name: "Team " + suffix, Slug: "team-mcp-" + suffix})
	user := mustCreateUser(t, d, CreateUserParams{Email: suffix + "@example.com", DisplayName: "User " + suffix})
	key := mustCreateAPIKey(t, d, testKeyParams(org.ID, user.ID, team.ID, user.ID))
	return org.ID, team.ID, key.ID, svrA.ID, svrB.ID
}

// ---- CheckMCPAccess -----------------------------------------------------------

// TestCheckMCPAccess_OrgNoEntries_Denied verifies the closed-by-default org policy:
// an org with no org_mcp_access entries must deny access to any server.
func TestCheckMCPAccess_OrgNoEntries_Denied(t *testing.T) {
	t.Parallel()

	d := openMigratedDB(t)
	orgID, teamID, keyID, serverA, _ := setupMCPAccessFixture(t, d, "no-entries")

	got, err := d.CheckMCPAccess(context.Background(), orgID, teamID, keyID, serverA)
	if err != nil {
		t.Fatalf("CheckMCPAccess() error = %v", err)
	}
	if got {
		t.Error("CheckMCPAccess() = true, want false (org is closed-by-default with no entries)")
	}
}

