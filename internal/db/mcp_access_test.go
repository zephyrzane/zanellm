// TestLoadAllMCPAccess_Empty verifies that LoadAllMCPAccess returns empty maps
// when no access entries exist.
func TestLoadAllMCPAccess_Empty(t *testing.T) {
	t.Parallel()

	d := openMigratedDB(t)

	org, team, key, err := d.LoadAllMCPAccess(context.Background())
	if err != nil {
		t.Fatalf("LoadAllMCPAccess() error = %v", err)
	}
	if len(org) != 0 {
		t.Errorf("org map len = %d, want 0", len(org))
	}
	if len(team) != 0 {
		t.Errorf("team map len = %d, want 0", len(team))
	}
	if len(key) != 0 {
		t.Errorf("key map len = %d, want 0", len(key))
	}
}

// TestLoadAllMCPAccess_PopulatesAllMaps verifies that LoadAllMCPAccess returns
// correct entries for org, team, and key scopes after data is inserted.
func TestLoadAllMCPAccess_PopulatesAllMaps(t *testing.T) {
	t.Parallel()

	d := openMigratedDB(t)
	orgID, teamID, keyID, serverA, serverB := setupMCPAccessFixture(t, d, "load-all")
	mustSetOrgMCPAccess(t, d, orgID, []string{serverA, serverB})
	mustSetTeamMCPAccess(t, d, teamID, []string{serverA})
	mustSetKeyMCPAccess(t, d, keyID, []string{serverB})

	org, team, key, err := d.LoadAllMCPAccess(context.Background())
	if err != nil {
		t.Fatalf("LoadAllMCPAccess() error = %v", err)
	}

	orgServers := org[orgID]
	if len(orgServers) != 2 {
		t.Errorf("org[%q] len = %d, want 2", orgID, len(orgServers))
	}

	teamServers := team[teamID]
	if len(teamServers) != 1 || teamServers[0] != serverA {
		t.Errorf("team[%q] = %v, want [%s]", teamID, teamServers, serverA)
	}

	keyServers := key[keyID]
	if len(keyServers) != 1 || keyServers[0] != serverB {
		t.Errorf("key[%q] = %v, want [%s]", keyID, keyServers, serverB)
	}
}

// ---- SetOrgMCPAccess / GetOrgMCPAccess round-trip ----------------------------

// TestSetOrgMCPAccess_ReplacesExisting verifies that calling SetOrgMCPAccess a
// second time atomically replaces the previous allowlist.
func TestSetOrgMCPAccess_ReplacesExisting(t *testing.T) {
	t.Parallel()

	d := openMigratedDB(t)
	svrA := mustCreateMCPServer(t, d, defaultMCPParams("replace-a"))
	svrB := mustCreateMCPServer(t, d, defaultMCPParams("replace-b"))
	svrC := mustCreateMCPServer(t, d, defaultMCPParams("replace-c"))
	org := mustCreateOrg(t, d, CreateOrgParams{Name: "Replace Org", Slug: "replace-org-mcp"})

	mustSetOrgMCPAccess(t, d, org.ID, []string{svrA.ID, svrB.ID})
	mustSetOrgMCPAccess(t, d, org.ID, []string{svrC.ID}) // replace

	got, err := d.GetOrgMCPAccess(context.Background(), org.ID)
	if err != nil {
		t.Fatalf("GetOrgMCPAccess() error = %v", err)
	}
	if len(got) != 1 || got[0] != svrC.ID {
		t.Errorf("GetOrgMCPAccess() = %v, want [%s]", got, svrC.ID)
	}
}

